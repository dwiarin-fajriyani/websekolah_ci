                        <p>Kepala SMKN 2 Sumedang, Drs.H.Edi  Supriadi,M.Pd, dalam sambutannya berharap terpilih Ketua OSIS dan Ketua MPK yang lebih baik dan berkualitas dalam memimpin roda organisasi OSIS dan MPK satu tahun ke depan. Dari hasil penghitungan suara, terpilih dengan suara terbanyak untuk Ketua OSIS adalah kandidat nomor urut 6 yaitu Aan Yuningsih dan suara terbanyak untuk Ketua MPK adalah kandidat nomor urut 6 yaitu Nadia Maharani. </p>
                        
                        <p>Adapun rincian perolehan suara dilihat dari angka partisipasinya, dari total 1721 daftar pemilih tetap (DPT), suara yang masuk sejumlah 1636 , dan suara yang tidak masuk sejumlah 85. </p>
