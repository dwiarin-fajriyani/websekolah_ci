                        <p>SMKN 2 SUMEDANG, menggelar Pemilihan Ketua OSIS dan Ketua MPK masa bakti 2020-2021 pada Jumâ€™at (20/11/2020). Pilketos yang dilakukan  melalui e-voting dianggap menjadi pilihan yang harus dilakukan di masa pandemi Covid-19 saat ini. Selain bisa menghindari kerumunan dan mencegah penyebaran virus, cara ini dirasa efektif dan efisien dalam implementasinya. </p>
                        <p>Yang membanggakan, lanjut Pa Edi, semua proses dilakukan panitia yang merupakan siswa SMKN 2 Sumedang secara mandiri dengan didampingi para pembina OSIS. Untuk menjaga independensi, panitia khususnya admin teknologi informasi diwajibkan membuat pakta integritas sehingga asas Luberjurdil bisa tergaransi. </p>
                        
