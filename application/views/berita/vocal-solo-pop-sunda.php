                        <h4 class="mb-50">Vocal Solo Pop Sunda Digelar SMKN 2 Sumedang</h4>
                        <img src="<?php echo base_url('assets/img/bg-img/blog/vocal-1.jpg'); ?>" alt="">
                        <p>SMKN 2 Sumedang menggelar perlombaan Vocal Solo Pop Sunda, antar SMP melalui Gebyar Pertunjukan Expo. </p>
                        <p>Kegiatan tersebut, mengambil tema “Meningkatkan Kreativitas untuk Generasi yang Berkualitas,”. </p>
                        
                        <p>Acara, digelar di Lapangan Upacara Kampus SMKN 2 Sumedang, Selasa 28 Januari 2020. </p>

                        <p>Kegiatan tersebut dihadiri Kabid PSMK Dinas Pendidikan Jawa Barat, Kepala KCD Dinas Pendidikan wilayah Priangan 8 Provinsi Jabar dan Dinkes Kabupaten Sumedang. </p>
                        
                        <p>Sekretaris Dinas Pendidikan Prov Jabar, Dian Sukmara mengatakan itu satu kesempatan bagi Kabupaten Sumedang untuk lebih besinergi dengan Provinsi Jawa Barat dalam rangka mengembangkan program-program dinas pendidikan kabupaten dan provinsi. </p>

                        <p>Ia menuturkan, pada setiap penyelenggarakan yang di sebut Cerdas Exotic, yakni semua sekolah dari SD sampai perguruan tinggi semua beruhkan ke Cerdas Exotic. Bahkan menurut Sekretari Dinas Pendidikan Kabupaten Sumedang, Edi Supriadi mengatakna, itu kegiatan menjelang uji kompetensi dan praktek jadi tiap jurusan melakukan praktek, dari Expo empat jurusan.</p>

                        <p> Yakni jurusan akuntasi, perkantoran, BDP atau Pemasaran dan Rekayasa Perangkat Lunak. “Peserta dari kreasi sengaja kami ajak dari dari SMP, dan pasinggiri untuk pop Sunda yakni dalam rangka promosi sekolah ke SMP – SMP,” ujarnya. Makanya untuk perlombaan Pop Sunda sengaja kami gelar tingkat SMP atau MTSN se-Kabupaten Sumedang, untuk pasanggiri digelar 2 tahun sekali.
                        </p>

