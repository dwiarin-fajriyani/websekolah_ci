                        <h4 class="mb-50">Membanggakan! SMKN 2 Sumedang raih penghargaan pada Dinamik 15 UPI</h4>
                        <img src="<?php echo base_url('assets/img/bg-img/blog/iwc.jpeg'); ?>" alt="">
                        <p>Para pemenang lomba pada Dinamik 15 (Dies Natalis Keluarga Besar Ilmu Komputer yang ke -15) yang diselenggarakan oleh Mahasiswa Departemen Pendidikan Ilmu Komputer Universitas Pendidikan Indonesia telah resmi diumumkan pada Minggu, 15 November 2020. </p>
                        <p>Terdapat beberapa bidang yang dilombakan pada event Dies Natalis tersebut, diantaranya Web Technology, Desain Poster, Animasi, serta Innovation Writing Contest. Semua bidang lomba tersebut  bertemakan tentang teknologi yang bertujuan untuk mengasah kemampuan peserta lomba dalam berpikir kritis dan inovatif serta menghasilkan ide yang dapat menanggulangi permasalahan yang ada. </p>
                        
                        <p>Kompetisi tahun 2020 ini diselenggarakan secara daring melalui berbagai platform untuk memudahkan para peserta dalam berkomunikasi agar tetap efektif mengikuti arahan panitia. Dalam ajang kompetisi yang mengangkat tema teknologi ini, diikuti peserta dari berbagai provinsi yang ada di Indonesia, mengingat kompetisi ini bertingkat nasional bagi SMA/SMK sederajat.</p>
                        <p>Dalam daftar pemenang, terdapat 1 tim dari SMK Negeri 2 Sumedang yakni tim Innovation No Limit untuk bidang lomba Innovation Writing  Contest yang diikuti oleh 3 orang per timnya. Tim Innovation No Limit berhasil mendapat juara kedua dari Innovation Writing Contest, dengan membuat sebuah inovasi untuk media pembelajaran berbasis teknologi yakni sebuah rancangan untuk Aplikasi NOFCATION. Aplikasi yang diusulkan tersebut merupakan sebuah hasil dari penelitian terhadap ratusan siswa di daerah Jawa Barat sebagai responden untuk meneliti permasalahan yang dihadapi saat masa pandemi seperti saat ini. </p>
                        
                        <p>“Selain untuk mengikuti lomba ini, kami harapkan ide yang telah diteliti ini dapat juga direalisasikan untuk membantu permasalahan yang terjadi selama pembelajaran jarak jauh”, tutur Nur Sapitri selaku ketua tim Innovation No Limit.</p>
